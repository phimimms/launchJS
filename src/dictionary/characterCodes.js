/* The HTML Character Codes for Symbols */

/* Braces */
export const LBRACE = String.fromCharCode(123);
export const RBRACE = String.fromCharCode(125);
/* Comments */
export const LCOMMENT = String.fromCharCode(47) + String.fromCharCode(42);
export const RCOMMENT = String.fromCharCode(42) + String.fromCharCode(47);
export const COMMENT = String.fromCharCode(47) + String.fromCharCode(47);
/* Comparator Symbols */
export const LESSTHAN = String.fromCharCode(60);
export const GREATERTHAN = String.fromCharCode(62);
/* Single Quotes */
export const LSQUOTE = String.fromCharCode(8216);
export const RSQUOTE = String.fromCharCode(8217);
export const SQUOTE = String.fromCharCode(39);
